/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	bool selectedled= true; //true led1, false led2

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_USART2_UART_Init();
  MX_ADC2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */

  if (HAL_TIM_Base_Start_IT(&htim3) != HAL_OK)
    {
      /* Starting Error */
      Error_Handler();
    }
  if (HAL_TIM_Base_Start_IT(&htim4) != HAL_OK)
    {
      /* Starting Error */
      Error_Handler();
    }

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	 switch (state){
	 case 'W': //WAITING STATE
		 ;
		 printinserial("Board in waiting state - please press the emergency button\r\n\n");
		 HAL_Delay(500);
		 break;

	 case 'R': //RUNNING STATE
		 switch(sensortoread){
		 case 'S':	 	//SENSOR
			 // Read analog value of sensor
			 HAL_ADC_Start(&hadc1);
			 HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
			 float valueadc = HAL_ADC_GetValue(&hadc1) * (3.3/4096) ; //Convert adc(0, 4096) to volt(0, 3.3)

			 //Print of analog value
			 char msg[50];
			 sprintf(msg, "Analog Value: %.2f V\r\n", valueadc);
			 printinserial(msg);


			 //Read digital value of sensor
			 GPIO_PinState statedig;
			 statedig = HAL_GPIO_ReadPin(GPIOA,GPIO_PIN_8);
			 bool valuedig = (statedig == GPIO_PIN_SET);

			 //Print of digital value
			 sprintf(msg, "Digital value: %d\r\n\n", valuedig);
			 printinserial(msg);

			 break;
		 case 'V':
			 //Read the input voltage
			 HAL_ADC_Start(&hadc2);
			 HAL_ADC_PollForConversion(&hadc2, HAL_MAX_DELAY);
			 float value = HAL_ADC_GetValue(&hadc2) * (3.3/4096); //Convert adc(0, 4096) to volt(0, 3.3)

			 //Print of analog value
			 sprintf(msg, "Voltage: %.2f V\r\n", value);
			 printinserial(msg);

			 //Check the value of led
			 if(value>2.7){
			 	//Danger state led 1
			 	selectedled=true;
			 	state='D';
			 }else{
			 	//Reset of led 1
			 	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_RESET);

			 	//Print info of led 1
			 	printinserial("led 1 off\r\n");
			 }if(value<1.8){
			 	//Danger state led 2
			 	selectedled=false;
			 	state='D';
			 }else{
			 	//Reset of led 2
			 	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_5, GPIO_PIN_RESET);

			 	//Print info of led 2
			 	printinserial("led 2 off\r\n");
			 }
			 break;
		 }
		 //Reset
		 sensortoread='N';
		 break;

	 case 'D': //DANGER STATE
		 if(selectedled){
			 //Set of led 1
			 HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_SET);

			 //Print state
			 printinserial("Danger state sovratensione, led 1 acceso\r\n\n");
		 }
		 else{
			 //Set of led 2
			 HAL_GPIO_WritePin(GPIOB, GPIO_PIN_5, GPIO_PIN_SET);

			 //Print state
			 printinserial("Danger state sottotensione, led 2 acceso\r\n\n");
		}
		 //Reset
		 selectedled=0;
		 state='R';
		break;
	 }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

